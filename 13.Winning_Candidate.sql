CREATE TABLE VOTE 
(
	ID INT AUTOINCREMENT,
    CANDIDATEID INT ,
    FOREIGN KEY(CANDIDATEID) REFERENCES CANDIDATE(ID)
);
INSERT INTO VOTE VALUES 
(1,2),
(2,4),
(3,3),
(4,2),
(5,5);

SELECT NAME FROM (
SELECT NAME , COUNT(CANDIDATEID) AS V_COUNT
FROM VOTE A 
JOIN CANDIDATE B 
ON B.ID = A.CANDIDATEID
GROUP BY 1
ORDER BY 1
LIMIT 1
);